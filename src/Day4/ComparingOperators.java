package Day4;

public class ComparingOperators {

    public static void main(String[] args) {
        int a = 20, b = 20;
        // >, <, >=, <=, ==, !=
        System.out.println(a>b); // false
        System.out.println(a<b); //false
        System.out.println(a>=b); // true
        System.out.println(a<=b); // true
        System.out.println(a==b); // true
        System.out.println(a!=b); // false
    }

}
