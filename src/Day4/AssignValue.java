package Day4;

public class AssignValue {
    public static void main(String[] args) {
        // Compound operators:
        int a = 50;
        a+=50; // a= a + 50;
        System.out.println(a); // 100
        a-=25; // a= a - 25;
        System.out.println(a); // 75
        a*=10; // a = a * 10;
        System.out.println(a); // 750
        a/=5; // a = a / 5;
        System.out.println(a); // 150
        a%=95; // a = a % 99;
        System.out.println(a); // 55

        String string = "someValue";
    }
}
