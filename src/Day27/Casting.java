package Day27;

public class Casting {
    public static void main(String[] args) {

        // Explicit casting - casting done by programmer
        int intNumber = 5;
        byte byteNumber = (byte) intNumber;

        // Implicit casting - casting done by JVM
        int intValue = 543543;
        long longValue = intValue;


         int number = (int)35.5;


    }
}
