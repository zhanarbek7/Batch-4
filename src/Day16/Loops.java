package Day16;

public class Loops {
    public static void main(String[] args) {
        // For loop and While loop
        // 1. Using for loop print numbers from 1 to 10
        // 2. Using while loop print numbers from 1 to 10
        for(int i = 1; i <= 10; i++){
            System.out.println(i);
        }
        System.out.println();
        int i = 1;
        while(i <= 10){
            System.out.println(i);
            i++;
        }




    }
}
